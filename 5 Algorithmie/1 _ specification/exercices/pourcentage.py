def pourcentage(a,b):
    """renvoie le pourcentage de a par rapport à b
    Paramètres d'entrée : 
        a -> int ou decimal : valeur du sous-ensemble
        b -> int ou decimal, b>0 : valeur de l'ensemble
        
    Paramètres de sortie : retourne --> int ou float : pourcentage
    
	"""
    return(a*100/b)
